int		ft_isprime(unsigned int nb)
{
	unsigned int	i;

	if (nb == 0 || nb == 1)
		return (0);
	i = 2;
	while ((i * i) <= nb)
	{
		if ((nb % i) == 0 && i != nb)
			return (0);
		i++;
	}
	return (1);
}
